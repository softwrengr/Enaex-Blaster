package com.techease.enaexblaster.models;

public class SdobModel {
    String diameter;
    String density;
    String holeLength;
    String stemLength;
    String row_name;
    String checkCalculator;

    public String getDiameter() {
        return diameter;
    }

    public void setDiameter(String diameter) {
        this.diameter = diameter;
    }

    public String getDensity() {
        return density;
    }

    public void setDensity(String density) {
        this.density = density;
    }

    public String getHoleLength() {
        return holeLength;
    }

    public void setHoleLength(String holeLength) {
        this.holeLength = holeLength;
    }

    public String getStemLength() {
        return stemLength;
    }

    public void setStemLength(String stemLength) {
        this.stemLength = stemLength;
    }

    public String getRow_name() {
        return row_name;
    }

    public void setRow_name(String row_name) {
        this.row_name = row_name;
    }

    public String getCheckCalculator() {
        return checkCalculator;
    }

    public void setCheckCalculator(String checkCalculator) {
        this.checkCalculator = checkCalculator;
    }
}
