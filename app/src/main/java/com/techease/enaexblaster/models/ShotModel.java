package com.techease.enaexblaster.models;

public class ShotModel {
    String rows;
    String holes;
    String diameter;
    String density;
    String burden;
    String spacing;
    String benchHeigh;
    String subDrill;
    String stemLength;
    String rockDensity;
    String holeMs;
    String distance;
    String scaling;
    String attenuation;
    String rowName;

    public String getRowName() {
        return rowName;
    }

    public void setRowName(String rowName) {
        this.rowName = rowName;
    }

    public String getRows() {
        return rows;
    }

    public void setRows(String rows) {
        this.rows = rows;
    }

    public String getHoles() {
        return holes;
    }

    public void setHoles(String holes) {
        this.holes = holes;
    }

    public String getDiameter() {
        return diameter;
    }

    public void setDiameter(String diameter) {
        this.diameter = diameter;
    }

    public String getDensity() {
        return density;
    }

    public void setDensity(String density) {
        this.density = density;
    }

    public String getBurden() {
        return burden;
    }

    public void setBurden(String burden) {
        this.burden = burden;
    }

    public String getSpacing() {
        return spacing;
    }

    public void setSpacing(String spacing) {
        this.spacing = spacing;
    }

    public String getBenchHeigh() {
        return benchHeigh;
    }

    public void setBenchHeigh(String benchHeigh) {
        this.benchHeigh = benchHeigh;
    }

    public String getSubDrill() {
        return subDrill;
    }

    public void setSubDrill(String subDrill) {
        this.subDrill = subDrill;
    }

    public String getStemLength() {
        return stemLength;
    }

    public void setStemLength(String stemLength) {
        this.stemLength = stemLength;
    }

    public String getRockDensity() {
        return rockDensity;
    }

    public void setRockDensity(String rockDensity) {
        this.rockDensity = rockDensity;
    }

    public String getHoleMs() {
        return holeMs;
    }

    public void setHoleMs(String holeMs) {
        this.holeMs = holeMs;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getScaling() {
        return scaling;
    }

    public void setScaling(String scaling) {
        this.scaling = scaling;
    }

    public String getAttenuation() {
        return attenuation;
    }

    public void setAttenuation(String attenuation) {
        this.attenuation = attenuation;
    }
}
